"""Compare layer fusion across 5 TRT engines and generate fusion_catalog.md.

Reads:
    logs/trt_build_fp32.log
    logs/trt_build_fp16.log
    logs/trt_build_int8_entropy.log
    logs/trt_build_int8_minmax_500.log
    logs/trt_build_int8_percentile_500.log

Writes:
    docs/fusion_catalog.md          — analysis as markdown (tables + observations)
    results/fusion/parsed.json      — raw parsed structure for future reuse

Run:
    python3 experiments/fusion_analysis.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from infra.analysis.fusion_parser import parse_build_log, save_parsed  # noqa: E402


# (display_name, log_path) — order controls table column order.
ENGINES: list[tuple[str, str]] = [
    ("fp32",             "logs/trt_build_fp32.log"),
    ("fp16",             "logs/trt_build_fp16.log"),
    ("int8_entropy",     "logs/trt_build_int8_entropy.log"),
    ("int8_minmax",      "logs/trt_build_int8_minmax_500.log"),
    ("int8_percentile",  "logs/trt_build_int8_percentile_500.log"),
]

# Canonical pipeline stages we expect in the LAST (real-build) phase.
# Order reflects the actual order TRT runs them in. Occurrence-indexed
# variants (#1, #2) are included because dupe/final-dead-layer/tensor-merging
# run BOTH before and after vertical fusions.
PIPELINE_STAGES = [
    "Original",
    "After dead-layer removal",
    "After Myelin optimization",
    "After scale fusion",
    "After dupe layer removal #1",
    "After final dead-layer removal #1",
    "After tensor merging #1",
    "After vertical fusions",
    "After dupe layer removal #2",
    "After final dead-layer removal #2",
    "After tensor merging #2",
    "After slice removal",
    "After concat removal",
    "After reformat layers",
]



# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _last_phase_pipeline_map(parsed: dict) -> dict[str, int]:
    """Return {stage_name -> last layer_count seen for that stage}
    within the LAST build phase (= the real engine build).
    """
    phases = parsed["build_phases"]
    if not phases:
        return {}
    last = phases[-1]
    out: dict[str, int] = {}
    for stage, n in last["pipeline"]:
        # Some stages repeat (e.g. "After final dead-layer removal" twice
        # within the same phase). Keep the LAST occurrence as the final state.
        out[stage] = n
    return out


def _all_fusion_types(parsed_all: dict[str, dict]) -> list[str]:
    """Union of fusion types across all engines, sorted by global frequency."""
    totals: dict[str, int] = {}
    for p in parsed_all.values():
        for t, n in p["totals"]["fusion_type_counts"].items():
            totals[t] = totals.get(t, 0) + n
    return sorted(totals.keys(), key=lambda t: -totals[t])


# ---------------------------------------------------------------------------
# Markdown renderers
# ---------------------------------------------------------------------------

def _md_header() -> str:
    return (
        "# Layer Fusion Catalog\n\n"
        "Analysis of TensorRT layer fusion across 5 YOLOv8n engines built\n"
        "from the same ONNX with different precision/calibration settings.\n"
        "Generated by `experiments/fusion_analysis.py` from `trtexec --verbose` logs.\n"
    )


def _md_meta_table(parsed_all: dict[str, dict]) -> str:
    lines = [
        "## Build metadata",
        "",
        "| engine | precision | phases | final layers | build time (s) |",
        "|---|---|---|---|---|",
    ]
    for name, _ in ENGINES:
        m = parsed_all[name]["meta"]
        bt = f"{m['build_time_sec']:.1f}" if m["build_time_sec"] is not None else "—"
        flc = m["final_layer_count"] if m["final_layer_count"] is not None else "—"
        lines.append(
            f"| {name} | {m['precision']} | {m['phase_count']} | {flc} | {bt} |"
        )
    lines.append("")
    lines.append(
        "INT8 builds have 2 phases (calibration run + real build); "
        "FP32/FP16 have 1.\n"
    )
    return "\n".join(lines)


def _md_pipeline_table(parsed_all: dict[str, dict]) -> str:
    """Layer-count pipeline across engines (LAST phase = real build)."""
    lines = [
        "## Layer-count pipeline (real engine build)",
        "",
        "Rows = pipeline stages, columns = engines, cells = layer count after\n"
        "that stage. For INT8 engines, this is the SECOND phase only (the\n"
        "calibration phase is excluded).",
        "",
    ]
    header = "| stage | " + " | ".join(name for name, _ in ENGINES) + " |"
    sep = "|---|" + "---|" * len(ENGINES)
    lines.append(header)
    lines.append(sep)

    maps = {name: _last_phase_pipeline_map(parsed_all[name]) for name, _ in ENGINES}

    for stage in PIPELINE_STAGES:
        row = [stage]
        for name, _ in ENGINES:
            v = maps[name].get(stage)
            row.append(str(v) if v is not None else "—")
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    return "\n".join(lines)


def _md_calibration_phase_table(parsed_all: dict[str, dict]) -> str:
    """INT8 only — the calibration phase pipeline (where fusion is skipped)."""
    int8_engines = [(n, p) for n, p in
                    [(name, parsed_all[name]) for name, _ in ENGINES]
                    if p["meta"]["precision"] == "int8" and p["meta"]["phase_count"] >= 2]
    if not int8_engines:
        return ""

    lines = [
        "## INT8 calibration phase (Phase 1)",
        "",
        "INT8 builds run the network once in calibration mode to collect\n"
        "activation statistics. Vertical fusions are SKIPPED in this phase\n"
        "(layer count stays at 248). Quant/dequant nodes are inserted\n"
        "(concat removal grows to ~279 layers). Phase 2 then uses the\n"
        "collected scales to do the real fusion-aware build.",
        "",
    ]
    header = "| stage | " + " | ".join(n for n, _ in int8_engines) + " |"
    sep = "|---|" + "---|" * len(int8_engines)
    lines.append(header)
    lines.append(sep)

    # First-phase pipelines.
    phase1_maps = {}
    for name, p in int8_engines:
        ph1 = p["build_phases"][0]
        m: dict[str, int] = {}
        for stage, n in ph1["pipeline"]:
            m[stage] = n
        phase1_maps[name] = m

    for stage in PIPELINE_STAGES:
        # Only show stages that appear in at least one engine's phase 1.
        if not any(stage in phase1_maps[n] for n, _ in int8_engines):
            continue
        row = [stage]
        for n, _ in int8_engines:
            v = phase1_maps[n].get(stage)
            row.append(str(v) if v is not None else "—")
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    return "\n".join(lines)


def _md_fusion_counts_table(parsed_all: dict[str, dict]) -> str:
    """Fusion type counts across engines."""
    lines = [
        "## Fusion type counts",
        "",
        "Number of times each fusion type fired across the whole build log.\n"
        "Generic conv/pointwise patterns dominate; structural fusions\n"
        "(ConstShuffleFusion, ShuffleShuffleFusion) are rare.",
        "",
    ]
    header = "| fusion type | " + " | ".join(name for name, _ in ENGINES) + " | total |"
    sep = "|---|" + "---|" * (len(ENGINES) + 1)
    lines.append(header)
    lines.append(sep)

    types_sorted = _all_fusion_types(parsed_all)
    for t in types_sorted:
        row = [t]
        total = 0
        for name, _ in ENGINES:
            n = parsed_all[name]["totals"]["fusion_type_counts"].get(t, 0)
            row.append(str(n))
            total += n
        row.append(str(total))
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    return "\n".join(lines)


def _md_int8_variants_comparison(parsed_all: dict[str, dict]) -> str:
    """Direct check that the 3 INT8 calibration variants produce identical
    fusion decisions — supports the §4.2 conclusion that mAP differences
    come from quantization scales, not from fusion/kernel choice.
    """
    int8_names = [n for n, _ in ENGINES if n.startswith("int8_")]
    if len(int8_names) < 2:
        return ""

    lines = [
        "## INT8 variants: do calibration methods change fusion?",
        "",
        "If three INT8 engines (built with different calibration methods\n"
        "but the same calibration data size) show identical layer counts\n"
        "and fusion counts, then mAP differences observed between them\n"
        "are attributable to quantization SCALES alone — not to the\n"
        "compiler choosing different fusion/kernel paths.",
        "",
    ]

    # 1) Final-stage layer counts.
    lines.append("### Final layer counts at each pipeline stage")
    lines.append("")
    header = "| stage | " + " | ".join(int8_names) + " |"
    sep = "|---|" + "---|" * len(int8_names)
    lines.append(header)
    lines.append(sep)
    maps = {n: _last_phase_pipeline_map(parsed_all[n]) for n in int8_names}
    for stage in PIPELINE_STAGES:
        if not any(stage in maps[n] for n in int8_names):
            continue
        row = [stage]
        for n in int8_names:
            v = maps[n].get(stage)
            row.append(str(v) if v is not None else "—")
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")

    # 2) Fusion type counts.
    lines.append("### Fusion type counts")
    lines.append("")
    header = "| fusion type | " + " | ".join(int8_names) + " |"
    sep = "|---|" + "---|" * len(int8_names)
    lines.append(header)
    lines.append(sep)
    all_types: set[str] = set()
    for n in int8_names:
        all_types.update(parsed_all[n]["totals"]["fusion_type_counts"].keys())
    for t in sorted(all_types):
        row = [t]
        for n in int8_names:
            row.append(str(parsed_all[n]["totals"]["fusion_type_counts"].get(t, 0)))
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")

    # 3) Verdict.
    counts_vals = [
        tuple(sorted(parsed_all[n]["totals"]["fusion_type_counts"].items()))
        for n in int8_names
    ]
    fusions_identical = all(v == counts_vals[0] for v in counts_vals)

    # Separate "fusion decisions" (vertical fusions, slice removal) from
    # "post-fusion graph cleanup" (concat removal, reformat layers).
    FUSION_STAGES = ["After scale fusion", "After vertical fusions", "After slice removal"]
    CLEANUP_STAGES = ["After concat removal", "After reformat layers"]

    fusion_vals = [
        tuple((s, maps[n].get(s)) for s in FUSION_STAGES)
        for n in int8_names
    ]
    cleanup_vals = [
        tuple((s, maps[n].get(s)) for s in CLEANUP_STAGES)
        for n in int8_names
    ]
    fusion_identical = all(v == fusion_vals[0] for v in fusion_vals)
    cleanup_identical = all(v == cleanup_vals[0] for v in cleanup_vals)

    lines.append("### Verdict")
    lines.append("")
    if fusion_identical and fusions_identical:
        lines.append(
            f"**Fusion decisions are identical across the three INT8 variants.** "
            f"Stages compared: {', '.join(FUSION_STAGES)}. "
            "Fusion-type counts are also identical.\n"
        )
        if cleanup_identical:
            lines.append(
                "Post-fusion graph cleanup (concat removal, reformat layers) "
                "is also identical.\n"
            )
        else:
            lines.append(
                "Post-fusion graph cleanup differs slightly across variants "
                "(concat removal / reformat layers). This is a byproduct of "
                "how quant/dequant nodes are resolved during the calibration "
                "step (entropy/minmax went through the 2-phase calibrator API; "
                "percentile used a cache-injection 1-phase path) and is "
                "**unrelated to fusion decisions**.\n"
            )
        lines.append(
            "Together with §4.2 (per-tensor quantization scales differ by up "
            "to 8× across calibration methods), this isolates the mAP gap "
            "between minmax (0.380) and entropy/percentile (0.266–0.269) "
            "to quantization scales alone — not the compiler choosing "
            "different fusion or kernel paths.\n"
        )
    else:
        lines.append(
            "Fusion decisions are NOT identical across the three INT8 "
            "variants:\n"
            f"  - fusion-stage layer counts identical: {fusion_identical}\n"
            f"  - fusion-type counts identical:        {fusions_identical}\n\n"
            "Investigate which fusions / which stages differ before drawing\n"
            "conclusions about the source of the mAP gap.\n"
        )
    return "\n".join(lines)


def _md_observations(parsed_all: dict[str, dict]) -> str:
    """Hand-derived observations from the parsed data."""
    last = {n: _last_phase_pipeline_map(parsed_all[n]) for n, _ in ENGINES}

    fp32_vert = last["fp32"].get("After vertical fusions")
    fp16_vert = last["fp16"].get("After vertical fusions")
    int8_vert = last["int8_entropy"].get("After vertical fusions")

    fp16_reformat = last["fp16"].get("After reformat layers")
    int8_reformat = last["int8_entropy"].get("After reformat layers")

    lines = [
        "## Observations",
        "",
        "1. **Vertical fusion is the dominant size reduction.** "
        f"248 layers → FP32 {fp32_vert} → FP16 {fp16_vert} → INT8 {int8_vert}. "
        "Higher quantization enables more vertical fusion, not less. "
        "INT8 ends up smaller than FP16, contrary to a common assumption "
        "that quantization fragments the graph.",
        "",
        "2. **INT8 builds run a calibration phase first.** "
        "Phase 1 leaves the network at 248 layers (vertical fusion skipped) "
        "but inserts quant/dequant nodes (concat removal grows to ~279). "
        "Phase 2 then applies full fusion with the calibrated scales.",
        "",
        "3. **Reformat layers track precision boundaries.** "
        f"FP16 has {fp16_reformat} layers after reformat, INT8 has {int8_reformat}. "
        "Reformats are dtype-conversion stubs inserted where a layer's "
        "input/output precision differs from the surrounding graph. INT8 "
        "needing fewer reformats than FP16 is one structural reason its "
        "kernel-level runtime is faster.",
        "",
        "4. **Calibration method does not affect fusion.** "
        "The three INT8 engines (entropy / minmax / percentile) produce "
        "identical fusion decisions at every fusion stage (vertical fusions, "
        "slice removal) and identical fusion-type counts. Post-fusion graph "
        "cleanup (concat removal, reformat layers) differs by 2-5 layers "
        "across variants, but that reflects how quant/dequant nodes are "
        "resolved during calibration (a 2-phase calibrator API vs a "
        "1-phase cache-injection path) — not the compiler picking different "
        "fusion paths. Combined with §4.2 (quantization scales differ by up "
        "to 8× across methods), this isolates the mAP gap to quantization "
        "scales alone.",
        "",
        "5. **Fusion type distribution is precision-invariant.** "
        "PointWiseFusion fires the same number of times (65) across FP16 "
        "and all INT8 variants. GenericConvActFusion fires slightly more "
        "in INT8 (50 vs 44 in FP16), suggesting that quantized Conv outputs "
        "interact more uniformly with the following activation.",
        "",
    ]
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> int:
    print("Parsing TRT build logs...")
    parsed_all: dict[str, dict] = {}
    for name, log_path in ENGINES:
        path = Path(log_path)
        if not path.exists():
            print(f"  [warn] {log_path} missing — skipping {name}")
            continue
        print(f"  {name:18s} <- {log_path}")
        parsed_all[name] = parse_build_log(path)

    if len(parsed_all) < 2:
        print("Not enough logs parsed; aborting.")
        return 1

    print("\nWriting parsed.json...")
    out_json = save_parsed(
        {n: parsed_all[n] for n in parsed_all},
        Path("results/fusion/parsed.json"),
    )
    print(f"  -> {out_json}")

    print("\nGenerating markdown...")
    sections = [
        _md_header(),
        _md_meta_table(parsed_all),
        _md_pipeline_table(parsed_all),
        _md_calibration_phase_table(parsed_all),
        _md_fusion_counts_table(parsed_all),
        _md_int8_variants_comparison(parsed_all),
        _md_observations(parsed_all),
    ]
    md = "\n".join(s for s in sections if s)

    out_md = Path("docs/fusion_catalog.md")
    out_md.parent.mkdir(parents=True, exist_ok=True)
    out_md.write_text(md)
    print(f"  -> {out_md}")

    print("\nDone.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
